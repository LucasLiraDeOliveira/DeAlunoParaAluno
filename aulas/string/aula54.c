void alfabetico(char variavel)
{
    if((variavel >= 'a' && variavel <= 'z') || (variavel >= 'A' && variavel <= 'Z'))
        printf("%c e' uma letra\n", variavel);
    else
        printf("%c nao e' uma letra\n", variavel);
    return 0;
}

